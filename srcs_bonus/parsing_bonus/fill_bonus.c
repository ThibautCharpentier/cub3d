#include "../cub3d_bonus.h"

void	fill_map(t_win *win)
{
	int	x;
	int	y;

	y = -1;
	while (win->map.map[++y])
	{
		x = -1;
		while (win->map.map[y][++x])
		{
			if (win->map.map[y][x] == ' ')
				win->map.map[y][x] = '1';
		}
	}
}
